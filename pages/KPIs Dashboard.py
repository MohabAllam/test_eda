# Customer Demographics
col1, col2 = st.columns(2)

with col1:
    st.markdown("<div class='section-title'>📊 Age Distribution of Customers</div>", unsafe_allow_html=True)
    
    fig_age = px.histogram(df, x='age', nbins=20,
                          title='Customer Age Distribution',
                          color_discrete_sequence=['#1f77b4'])
    fig_age.update_layout(height=400, showlegend=False, template='plotly_white')
    fig_age.update_xaxes(title='Age')
