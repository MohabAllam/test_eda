# Revenue Trends
st.markdown("<div class='section-title'>💰 Monthly Revenue Trend</div>", unsafe_allow_html=True)

df['month'] = df['order_date'].dt.strftime('%Y-%m')
monthly_revenue = df.groupby('month')['total_price'].sum().reset_index()
monthly_revenue.columns = ['Month', 'Revenue']

fig_revenue = px.line(monthly_revenue, x='Month', y='Revenue', 
                     title='Revenue Growth Over Time',
                     markers=True, line_shape='spline',
                     labels={'Revenue': 'Total Revenue ($)', 'Month': 'Month'})
fig_revenue.update_traces(line=dict(color='#1f77b4', width=3), marker=dict(size=8))
fig_revenue.update_layout(hovermode='x unified', height=400, template='plotly_white')
st.plotly_chart(fig_revenue, use_container_width=True)

# Product Performance & Customer Insights
col1, col2 = st.columns(2)

with col1:
    st.markdown("<div class='section-title'>🏆 Top 5 Best Selling Products</div>", unsafe_allow_html=True)
    
    top_products = df.groupby('product_name').agg({
        'sales_id': 'count',
        'total_price': 'sum'
    }).reset_index()
    top_products.columns = ['Product', 'Orders', 'Revenue']
    top_products = top_products.sort_values('Revenue', ascending=False).head(5)
    
    fig_products = px.bar(top_products, x='Revenue', y='Product', 
                         orientation='h', 
                         title='Revenue Generated by Top 5 Products',
                         color='Revenue', color_continuous_scale='Blues')
    fig_products.update_layout(height=400, showlegend=False, template='plotly_white')
    st.plotly_chart(fig_products, use_container_width=True)

with col2:
    st.markdown("<div class='section-title'>👥 Top 5 Customers by Spending</div>", unsafe_allow_html=True)
    
    top_customers = df.groupby('customer_name').agg({
        'total_price': 'sum',
        'sales_id': 'count'
    }).reset_index()
    top_customers.columns = ['Customer', 'Total Spent', 'Orders']
    top_customers = top_customers.sort_values('Total Spent', ascending=False).head(5)
    
    fig_customers = px.bar(top_customers, x='Total Spent', y='Customer',
                          orientation='h',
                          title='Top 5 Customers by Total Spending',
                          color='Total Spent', color_continuous_scale='Blues')
    fig_customers.update_layout(height=400, showlegend=False, template='plotly_white')
    st.plotly_chart(fig_customers, use_container_width=True)

# Product Type & Gender Analysis
col1, col2 = st.columns(2)

with col1:
    st.markdown("<div class='section-title'>📦 Sales by Product Type</div>", unsafe_allow_html=True)
    
    product_sales = df.groupby('product_type').agg({
        'sales_id': 'count',
        'total_price': 'sum'
    }).reset_index()
    product_sales.columns = ['Product Type', 'Orders', 'Revenue']
    product_sales = product_sales.sort_values('Revenue', ascending=False)
    
    fig_product_type = px.pie(product_sales, values='Revenue', names='Product Type',
                             title='Revenue Distribution by Product Type',
                             color_discrete_sequence=px.colors.sequential.Blues)
    fig_product_type.update_layout(height=400, template='plotly_white')
    st.plotly_chart(fig_product_type, use_container_width=True)

